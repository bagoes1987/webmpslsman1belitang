const SISWA_DB = JSON.parse(localStorage.getItem('sapa_siswa_db'));

const AUTH_SESSION_KEY = "sapa_siswa_session";

// ── Fungsi Login ──
function loginSiswa(username, password) {
  const siswa = SISWA_DB.find(
    s => s.username.toUpperCase() === username.toUpperCase() && 
         s.password.toUpperCase() === password.trim().toUpperCase()
  );
  if (siswa) {
    sessionStorage.setItem(AUTH_SESSION_KEY, JSON.stringify({
      username: siswa.username,
      nama: siswa.nama,
      peleton: siswa.peleton,
      gender: siswa.gender,
      asal_sekolah: siswa.asal_sekolah,
      nisn: siswa.nisn,
      nis: siswa.nis,
      ttl: siswa.ttl,
      no_wa: siswa.no_wa,
      loggedInAt: new Date().toISOString()
    }));
    return { success: true, nama: siswa.nama };
  }
  return { success: false };
}

// ── Cek apakah sudah login ──
function isLoggedIn() {
  return sessionStorage.getItem(AUTH_SESSION_KEY) !== null;
}

// ── Ambil data siswa yang sedang login ──
function getSiswaSession() {
  const data = sessionStorage.getItem(AUTH_SESSION_KEY);
  return data ? JSON.parse(data) : null;
}

// ── Logout ──
function logoutSiswa() {
  sessionStorage.removeItem(AUTH_SESSION_KEY);
}

function requireLogin() {
  if (!isLoggedIn()) {
    window.location.href = "login_siswa.html";
  } else {
    const filename = window.location.pathname.split("/").pop();
    const redirectPages = ["ketentuan.html", "pelaksanaan.html", "fasilitas.html"];
    if (redirectPages.includes(filename)) {
      let tab = "profil";
      if (filename === "ketentuan.html") tab = "ketentuan";
      else if (filename === "pelaksanaan.html") tab = "pelaksanaan";
      else if (filename === "fasilitas.html") tab = "kegiatan";
      
      window.location.href = `dashboard_siswa.html?tab=${tab}`;
    }
  }
}

// ── Update profile siswa di database local ──
function updateSiswaProfile(username, profileData) {
  const db = JSON.parse(localStorage.getItem('sapa_siswa_db') || '[]');
  const index = db.findIndex(s => s.username.toUpperCase() === username.toUpperCase());
  if (index !== -1) {
    db[index] = { ...db[index], ...profileData };
    localStorage.setItem('sapa_siswa_db', JSON.stringify(db));
    
    // Update active session
    const currentSession = getSiswaSession();
    if (currentSession && currentSession.username.toUpperCase() === username.toUpperCase()) {
      sessionStorage.setItem(AUTH_SESSION_KEY, JSON.stringify({
        ...currentSession,
        ...profileData
      }));
    }
    return { success: true };
  }
  return { success: false };
}
